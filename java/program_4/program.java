class ArithmeticExample {
  public static void main(String[] args) {
    int num1 = 10;
    int num2 = 5;

    int sum = num1 + num2;
    int diff = num1 - num2;
    int prod = num1 * num2;
    int quot = num1 / num2;
    int mod = num1 % num2;

    System.out.println("sum: " + sum);
    System.out.println("diff: " + diff);
    System.out.println("prod: " + prod);
    System.out.println("quot: " + quot);
    System.out.println("mod: " + mod);
  }
}
