class VariableExample {
    public static void main(String[] args) {
        int num = 10;
        double dec = 3.14;
        char letter = 'A';
        boolean flag = true;
        String name = "John";

        System.out.println("num: " + num);
        System.out.println("dec: " + dec);
        System.out.println("letter: " + letter);
        System.out.println("flag: " + flag);
        System.out.println("name: " + name);
    }
}
